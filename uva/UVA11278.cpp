#include <bits/stdc++.h>
using namespace std;

map<char, char> QtoD= {
        // a - z
        {'a', '7'}, {'b', 'i'}, {'c', 'x'}, {'d', '9'},
        {'e', '6'}, {'f', 'a'}, {'g', 'e'}, {'h', 'h'},
        {'i', 'u'}, {'j', 't'}, {'k', 'd'}, {'l', 'c'},
        {'m', 'w'}, {'n', 'n'}, {'o', 'y'}, {'p', 'b'},
        {'q', '4'}, {'r', '.'}, {'s', '8'}, {'t', 'o'},
        {'u', 's'}, {'v', ','}, {'w', '5'}, {'x', 'z'},
        {'y', 'r'}, {'z', '0'},

        // A-Z
        {'A', '&'}, {'B', 'I'}, {'C', 'X'}, {'D', '('},
        {'E', '^'}, {'F', 'A'}, {'G', 'E'}, {'H', 'H'},
        {'I', 'U'}, {'J', 'T'}, {'K', 'D'}, {'L', 'C'},
        {'M', 'W'}, {'N', 'N'}, {'O', 'Y'}, {'P', 'B'},
        {'Q', '$'}, {'R', '>'}, {'S', '*'}, {'T', 'O'},
        {'U', 'S'}, {'V', '<'}, {'W', '%'}, {'X', 'Z'},
        {'Y', 'R'}, {'Z', ')'},

        // 0-9
        {'0', '/'}, {'1', '1'}, {'2', '2'}, {'3', '3'}, {'4', 'q'},
        {'5', 'j'}, {'6', 'l'}, {'7', 'm'}, {'8', 'f'}, {'9', 'p'},

        //other
        {' ', ' '}, {'`', '`'}, {'-', '['}, {'=', ']'},
        {'[', ';'}, {']', '='}, {';', 'k'}, {'\'', '-'},
        {',', 'v'}, {'.', 'g'}, {'/', '\''}, {'{', ':'}, {'}', '+'},

        {'~', '~'}, {'!', '!'}, {'@', '@'}, {'#', '#'}, {'$', 'Q'},
        {'%', 'J'}, {'^', 'L'}, {'&', 'M'}, {'*', 'F'}, {'(', 'P'},
        {')', '?'}, {'_', '{'}, {'+', '}'}, {'<', 'V'}, {'>', 'G'},
        {'?', '\"'}, {':', 'K'}, {'\"', '_'}, {'\\', '\\'}, {'|', '|'}
};

int main() {
    string s;
    while (getline(cin, s)) {
        for (char c: s) {
            if (QtoD.find(c) != QtoD.end()) {
                cout << QtoD[c];
            } else {
                cout << '?';
            }
        }
        cout << endl;
    }
    return 0;
}